# 生成诗人性格描述的 prompt 模板
_POET_STYLE_GEN_PROMPT = """请根据历史资料，为以下古代诗人生成一段用于AI角色扮演的性格与说话风格描述。

诗人：{poet_name}（{dynasty}代）

要求：
1. 基于真实的历史记载和生平事迹，不要编造
2. 描述要具体，包含以下四个维度：
   - 真实性格特点（用具体的行为或习惯体现，不要只说豪迈温柔这类抽象词）
   - 说话方式（语速、语气、用词习惯、是否喜欢打比方等）
   - 标志性的表达风格（什么时候特别有感情，喜欢用什么类型的意象）
   - 面对3到7岁小朋友时的调整方式
3. 格式：直接输出描述段落，不要分点，不要标题，不要解释，200字以内
4. 开头格式固定为：你是{poet_name}，{dynasty}代诗人……

只输出描述本身，不要有任何其他内容。"""


def get_poet_style(poet_name: str, dynasty: str) -> str:
    """
    获取诗人性格描述。
    优先从内存缓存读取；没有则查种子库；种子库也没有则动态生成并缓存。
    """
    # 1. 内存缓存命中
    if poet_name in _poet_style_cache:
        return _poet_style_cache[poet_name]

    # 2. 种子库命中，存入缓存后返回
    if poet_name in _POET_STYLE_SEEDS:
        _poet_style_cache[poet_name] = _POET_STYLE_SEEDS[poet_name]
        return _POET_STYLE_SEEDS[poet_name]

    # 3. 动态生成
    print(f"[诗人性格] 首次遇到「{poet_name}」，正在生成性格描述...")
    try:
        prompt = _POET_STYLE_GEN_PROMPT.format(poet_name=poet_name, dynasty=dynasty)
        url = "https://api-ai.vivo.com.cn/v1/chat/completions"
        headers = {
            "Content-Type": "application/json",
            "Authorization": f"Bearer {VIVO_APP_KEY}",
        }
        data = {
            "requestId": str(uuid.uuid4()),
            "model": "Volc-DeepSeek-V3.2",
            "messages": [{"role": "user", "content": prompt}],
        }
        response = requests.post(url, json=data, headers=headers, timeout=30)
        result = response.json()
        style = result["choices"][0]["message"]["content"].strip()
        style = strip_think_tags(style)
        _poet_style_cache[poet_name] = style
        print(f"[诗人性格] 「{poet_name}」生成完成，已缓存。")
        return style

    except Exception as e:
        # 生成失败时用兜底描述，不影响主流程
        fallback = (
            f"你是{dynasty}代诗人{poet_name}，性格温和有学问，"
            f"说话亲切有礼，对身边的事物有真实的感情，"
            f"面对小朋友时会把复杂的东西说得简单温暖。"
        )
        print(f"[诗人性格] 「{poet_name}」生成失败（{e}），使用兜底描述。")
        _poet_style_cache[poet_name] = fallback
        return fallback


# ── 年龄分层规则 ──────────────────────────────────────────────────────────────
#
# 分层依据：
#   皮亚杰认知发展理论：2-7岁为前运算阶段，其中4-5岁出现直觉思维，
#   5岁后开始有初步表象思维；语言发展里程碑：3岁词汇量约800-1000个，
#   6岁达3000-4000个，5岁是口语质变的第二个关键期；
#   情感理解发展：6-7岁才开始理解信念对情感的影响（如思乡、离别等抽象情感）。
#   综合以上，以5岁为分界线，分为小班中班档（3-4岁）和大班档（5-7岁）。
#
# ─────────────────────────────────────────────────────────────────────────────

def get_age_level(age: int) -> str:
    """根据年龄返回档位标识"""
    if age <= 4:
        return "low"    # 小班/中班：3-4岁
    else:
        return "high"   # 大班/学前班：5-7岁


